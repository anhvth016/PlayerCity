export const getPlayedStart = async () => {
	await pause(2000); // Pause 2s giả lập thời gian đợi dữ liệu trả về.
	return NewsDb.playedStart;
};

export const getPlayedId = async (playedId) => {
	await pause(2000);
	const playedIdResult = NewsDb.playedStartDetails.filter((played) => {
		return played.id === playedId;
	});
	return playedIdResult[0];
};

